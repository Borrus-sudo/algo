template <typename S, typename T>
void smax(S& a, const T& b) {
    if (a < b)
        a = b;
};

template <typename S, typename T>
void smin(S& a, const T& b) {
    if (a > b)
        a = b;
};

using ll = long long;
const int INF = 1e9 + 7;

void solve() {
    int n;
    cin >> n;
    vector<int> nums(n);
    for (int& num : nums)
        cin >> num;
    float maxi = 0;
    for (int right = 0; right < n; right++) {
        float ans = 0;
        for (int left = right; left < n; left++) {
            ans += nums[left];
            int len = left - right + 1;
            float avg = ans / len;
            smax(maxi, avg);
        }
    }
    cout << maxi << endl;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);

    int TC = 1;
    cin >> TC;

    while (TC--)
        solve();

    return 0;
}