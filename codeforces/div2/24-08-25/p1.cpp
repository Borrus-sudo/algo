using ll = long long;
const int INF = 1e9 + 7;

void solve()
{
    vector<bool> cache(101, false);
    int n;
    cin >> n;
    bool yes = false;
    while (n--)
    {
        int num;
        cin >> num;
        if (cache[num])
            yes = true;
        cache[num] = true;
    }
    if (yes)
        cout << "YES" << endl;
    else
        cout << "NO" << endl;
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);

    int TC = 1;
    cin >> TC;

    while (TC--)
        solve();

    return 0;
}